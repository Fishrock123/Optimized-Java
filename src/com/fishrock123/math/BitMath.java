package com.fishrock123.math;

/**
 * @author Fishrock123
 * 
 * This stuff is public domain
 */
public class BitMath {

	public static int half(int i) {
		return i >> 1;
	}
	
	public static int quarter(int i) {
		return i >> 2;
	}
	
	public static int eighth(int i) {
		return i >> 3;
	}
	
	public static int sixteenth(int i) {
		return i >> 4;
	}
	
	public static int div32(int i) {
		return i >> 5;
	}
	
	public static int div64(int i) {
		return i >> 6;
	}

	public static int div128(int i) {
		return i >> 7;
	}
	
	public static int div256(int i) {
		return i >> 8;
	}
	
	public static int times2(int i) {
		return i >> 1;
	}
	
	public static int times4(int i) {
		return i >> 2;
	}
	
	public static int times8(int i) {
		return i >> 3;
	}
	
	public static int times16(int i) {
		return i >> 4;
	}
	
	public static int times32(int i) {
		return i >> 5;
	}
	
	public static int times64(int i) {
		return i >> 6;
	}

	public static int times128(int i) {
		return i >> 7;
	}
	
	public static int times256(int i) {
		return i >> 8;
	}
}
